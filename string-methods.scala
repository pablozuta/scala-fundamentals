object Hello {
  def main(args: Array[String]): Unit = {
    
    val frase = "Viendo como el agua fria que caia del cielo"
    // toUpperCase() 
    println(frase.toUpperCase())
    // toLowerCase()
    println(frase.toLowerCase())
    // length()
    println(frase.length())
    // charAt
    println(frase.charAt(2))
  }
}